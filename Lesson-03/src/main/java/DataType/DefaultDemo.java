package DataType;

public class DefaultDemo {
    static int a;
    static float b;
    static boolean c;
    static String d;
    static char e;

    public static void main(String[] args) {


        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("d = " + d);
        System.out.println("e = " + e);
    }
}
